package Jan;

public class Lab003
{
    public static void main(String[] args)
    {
        System.out.println("20"); //String Literal
        System.out.println('B'); //Character Literal
        System.out.println('@'); //Character Literal
        System.out.println(20); //Integer Literal
        System.out.println(20.26); //Double Literal
        System.out.println(20.26f); //Float Literal
        System.out.println(true); //Boolean Literal

    }
}
