package Jan;

public class Lab0011 {
    public static void main(String[] args) {
        /*Local Variable
         * A variable which is declared within the method and within the class
         * Scope of local variable is from the beginning of the method till the end of the method
         * It cannot be classified as static or non-static
         * It cannot have default values
         * We cannot use the local variables before initialization
         * */

        int x;// Local Variables declaration
        x = 100;
        x = 200;
        System.out.println(x);

    }
}
