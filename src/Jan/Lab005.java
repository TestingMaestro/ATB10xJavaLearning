package Jan;

public class Lab005
{
    public static void main(String[] args)
    {
        //Variable Declaration
        int a;
        char myChar;
        float m;
        long n;
        double b;
        boolean isMarried;

        //Variable initialization
        a=10;
        myChar = 'F';
        m=45.3f;
        n=3445;
        b = 45.55;
        isMarried = true;

        //re-initialization
        a=20;
        myChar = 'J';
        m=45.2f;
        n=34555;
        b = 45.552;
        isMarried = false;

        //Variable Declaration and initialization in single line

        int x = 20;
        x=78;
        x = x + 1;
        int k = x; //assigning value of a variable to another variable
        System.out.println("Value of k is "+k);
    }
}
