package operators;

public class Lab0019
{
    public static void main(String[] args) {
        //Bitwise operators

        int a = 5;
        int b = 2;

        System.out.println(a&b);
        System.out.println(a|b);
        System.out.println(~b);
        System.out.println(a>>b);
        System.out.println(a<<b);
        System.out.println(a>>>b);
    }
}
