package operators;

public class Lab0017 {
    public static void main(String[] args) {
        // Relational operators
        int m = 23;
        int n = 22;
        int p = 23;

        System.out.println(m == p);
        System.out.println(m > n);
        System.out.println(m < n);
        System.out.println(m >= n);
        System.out.println(m <= n);
        System.out.println(m != n);
    }
}
